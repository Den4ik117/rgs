     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, User $user)
    {
        $validated = $request->validate([
            'role' => 'required|integer|in:' . implode(',', array_map(fn ($role) => $role->value, Role::ROLES)),
        ]);

        $user->update($validated);

